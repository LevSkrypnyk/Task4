# Фрагмент вірша Тараса Шевченка
text = """
Садок вишневий коло хати,
Хрущі над вишнями гудуть,
Плугатарі з плугами йдуть,
Співають, ідучи, дівчата,
А матері вечерять ждуть.
"""

# Використання вбудованих функцій для обробки тексту
text_lower = text.lower()  # Перетворення всіх символів на нижній регістр
words = text.split()  # Розбиття тексту на окремі слова
unique_words = set(words)  # Отримання унікальних слів у тексті

# Виведення результатів
print("Текст у нижньому регістрі:")
print(text_lower)
print("\nСписок усіх слів у тексті:")
print(words)
print("\nУнікальні слова у тексті:")
print(unique_words)
